#include "engine/plugin.h"

using namespace tubekit::engine;

plugin::plugin() : m_name(""), m_switch(false)
{
}

plugin::plugin(const std::string &name, bool flag) : m_name(name), m_switch(flag)
{
}

plugin::~plugin()
{
}

void plugin::set_name(const std::string &name)
{
    m_name = name;
}

const std::string &plugin::get_name() const
{
    return m_name;
}

void plugin::set_switch(bool flag)
{
    m_switch = flag;
}

bool plugin::get_switch() const
{
    return m_switch;
}
